package com.test.register.registration1.entity;

public class Faculty {
    private Long facultyId;
    private String facultyName;
    private String facultyEmail;
    private String password;
    private String facultyMobileNumber;
	public Long getFacultyId() {
		return facultyId;
	}
	public void setFacultyId(Long facultyId) {
		this.facultyId = facultyId;
	}
	public String getFacultyName() {
		return facultyName;
	}
	public void setFacultyName(String facultyName) {
		this.facultyName = facultyName;
	}
	public String getFacultyEmail() {
		return facultyEmail;
	}
	public void setFacultyEmail(String facultyEmail) {
		this.facultyEmail = facultyEmail;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getFacultyMobileNumber() {
		return facultyMobileNumber;
	}
	public void setFacultyMobileNumber(String facultyMobileNumber) {
		this.facultyMobileNumber = facultyMobileNumber;
	}

    // Getters and Setters
}
